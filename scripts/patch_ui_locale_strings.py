def patch_locale(locale: str, patches: dict[str, str]) -> int:
    path = RES / f"values-{locale}/strings.xml"
    text = path.read_text(encoding="utf-8")
    updated = 0
    for key, value in patches.items():
        escaped = (
            value.replace("&", "&amp;")
            .replace("<", "&lt;")
            .replace(">", "&gt;")
            .replace('"', "&quot;")
        )
        new_text, count = re.subn(
            rf'(<string name="{re.escape(key)}">)(.*?)(</string>)',
            rf"\1{escaped}\3",
            text,
            count=1,
            flags=re.DOTALL,
        )
        if count:
            text = new_text
            updated += 1
        else:
            print(f"  missing key {key} in {locale}")
    path.write_text(text, encoding="utf-8")
    return updated


def main() -> None:
    LOCALE_PATCHES["ar-rSA"] = dict(LOCALE_PATCHES["ar"])
    for locale, patches in LOCALE_PATCHES.items():
        count = patch_locale(locale, patches)
        print(f"{locale}: updated {count} strings")


if __name__ == "__main__":
    main()
