def locale_translations(locale: str) -> dict[str, str]:
    """Return translation overrides for a locale.

    Generic Arabic (`ar`) reuses the Saudi regional catalog (`ar-rSA`) so the
    loop never writes English fallbacks when only `ar-rSA` is populated.
    """
    if locale == "ar":
        return dict(T.get("ar") or T.get("ar-rSA") or {})
    return dict(T.get(locale, {}))


def parse_strings(path: Path) -> dict[str, str]:
    text = path.read_text(encoding="utf-8")
    return dict(re.findall(r'<string name="([^"]+)">(.*?)</string>', text, re.DOTALL))


def xml_escape(value: str) -> str:
    # Normalize any pre-escaped entities so "&amp;" does not become "&amp;amp;".
    value = html.unescape(value)
    return (
        value.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace('"', "&quot;")
    )


def append_missing(locale: str, translations: dict[str, str]) -> None:
    path = RES_DIR / f"values-{locale}/strings.xml"
    if not path.exists():
        print("skip missing locale file", locale)
        return
    default = parse_strings(DEFAULT)
    existing = parse_strings(path)
    missing = [k for k in default if k not in existing]
    if not missing:
        print(locale, "already complete")
        return
    lines = []
    for key in missing:
        value = translations.get(key, default[key])
        lines.append(f'    <string name="{key}">{xml_escape(value)}</string>')
    block = "\n".join(lines) + "\n"
    text = path.read_text(encoding="utf-8")
    if not text.rstrip().endswith("</resources>"):
        raise SystemExit(f"unexpected format: {path}")
    text = text.rstrip()[:-len("</resources>")] + block + "</resources>\n"
    path.write_text(text, encoding="utf-8")
    print(f"{locale}: appended {len(missing)} keys")


